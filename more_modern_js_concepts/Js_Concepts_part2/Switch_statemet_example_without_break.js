let day = 4;
switch (day) {
    case 0:
        console.log("Sunday")
        break;
    case 1:
        console.log("Monday");
        break;
    case 2:
        console.log("Tuesday");
        break;
    case 3:
        console.log("Wednesday");
        break;
    case 4:
        console.log("Thursday");    //Thursday

    case 5:
        console.log("Friday");  //Friday
        
    case 6:
        console.log("Saturday");    //Saturday
        
    default:
        console.log("Invalid"); //Invalid
        break;
}